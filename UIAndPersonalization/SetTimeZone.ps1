# Set the system's time zone to UTC. Replace "UTC" with the desired time zone ID.
# Example: To set the system's time zone to Eastern Standard Time (EST), use "Eastern Standard Time"

Set-TimeZone -Id "UTC"  # Sets the time zone to UTC

